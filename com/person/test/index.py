# 数据类型、命名规则、命名规范
a = 10
print(a)
a = 1 + 1

# int 类型
print(a)
print(type(a))

# bool 类型
a = True
print(type(a))

# 字符串类型
b = "b"
print(type(b))

# 元组类型
c = ()
print(type(c))

# 列表类型
d = []
print(type(d))

# 字典类型
e = {}
print(type(e))

